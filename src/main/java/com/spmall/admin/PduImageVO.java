package com.spmall.admin;

public class PduImageVO {
	private int pdu_code;
	private int pdu_image_code;
	private String pdu_imageFileName;
	private String pdu_imageFileType;
	private String pdu_register;
	
	public int getPdu_code() {
		return pdu_code;
	}
	public void setPdu_code(int pdu_code) {
		this.pdu_code = pdu_code;
	}
	public int getPdu_image_code() {
		return pdu_image_code;
	}
	public void setPdu_image_code(int pdu_image_code) {
		this.pdu_image_code = pdu_image_code;
	}
	public String getPdu_imageFileName() {
		return pdu_imageFileName;
	}
	public void setPdu_imageFileName(String pdu_imageFileName) {
		this.pdu_imageFileName = pdu_imageFileName;
	}
	public String getPdu_imageFileType() {
		return pdu_imageFileType;
	}
	public void setPdu_imageFileType(String pdu_imageFileType) {
		this.pdu_imageFileType = pdu_imageFileType;
	}
	public String getPdu_register() {
		return pdu_register;
	}
	public void setPdu_register(String pdu_register) {
		this.pdu_register = pdu_register;
	}
	
	
}
